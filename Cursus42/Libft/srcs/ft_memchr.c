void	*ft_memchr(void *str, int c, int n)
{
	int	i;

	i = 0;
	while (i < n)
	{
		if (((unsigned char *)str)[i] == (unsigned char)c)
			return (&str[i]);
		i++;
	}
	return (0);
}
