int	ft_there_is_c(char *str, char c)
{
	int	i;

	i = 0;
	if (!str || str[0] == 0)
		return (0);
	while (str[i] != 0 && str[i] != c)
		i++;
	if (str[i] == c)
		return (1);
	return (0);
}
