#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*ptr;

	if (lst)
	{
		while (*lst)
		{
			ptr = lst[0]->next;
			ft_lstdelone(lst[0], del);
			lst[0] = ptr;
		}
	}
	lst[0] = NULL;
}
