#include "libft.h"

void	ft_putnbr(int nb)
{
	long	nbr;

	nbr = (long)nb;
	if (nbr < 0)
		if (write(1, "-", 1))
			nbr = -nbr;
	if (nbr > 9)
		ft_putnbr(nbr / 10);
	if (write(1, &"0123456789"[nbr % 10], 1))
		return ;
}
