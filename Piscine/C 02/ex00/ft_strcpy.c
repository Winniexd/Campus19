char	*ft_strcpy(char *dest, char *src)
{
	int	i;
	int	len;

	len = 0;
	i = 0;
	while (src[len] != '\0')
	{
		len++;
	}
	while (i <= len)
	{
		dest[i] = src[i];
		i++;
	}
	return (dest);
}
